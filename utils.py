import random


def random_str(length):
    rand_str = ''
    chars = 'AaBbCcDdEeFfGgHhIiJjKkLlMmNnOoPpQqRrSsTtUuVvWwXxYyZz0123456789'
    for i in range(length):
        rand_str += chars[random.randint(0, len(chars)-1)]
    return rand_str
